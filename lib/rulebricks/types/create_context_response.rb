# frozen_string_literal: true

module Rulebricks
  module Types
    module CreateContextResponse
      # CreateContextResponse is an alias for ContextDetail

      # @option str [String]
      #
      # @return [untyped]
      def self.load(str)
        ::JSON.parse(str)
      end

      # @option value [untyped]
      #
      # @return [String]
      def self.dump(value)
        ::JSON.generate(value)
      end
    end
  end
end
